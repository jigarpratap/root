package Wrapper;


public class Wrapper{
      public static void main(String[] args) {
        Character a='a';// autoboxing
        char ch=a;//unboxing 
        System.out.println(ch);
        Integer num=12;
        int a1=num;
        System.out.println(a1);
        boolean b=false;
        Boolean c=b;
        System.out.println(c);
        double d=0.12;
        Double n=d;
        System.out.println(n);
       
      }
}